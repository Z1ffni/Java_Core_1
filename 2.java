public class MinMaxValues {
    public static void main(String[] args) {
        
        System.out.println("Мінімальне значення типу byte: " + Byte.MIN_VALUE);
        System.out.println("Максимальне значення типу byte: " + Byte.MAX_VALUE);

        System.out.println("Мінімальне значення типу short: " + Short.MIN_VALUE);
        System.out.println("Максимальне значення типу short: " + Short.MAX_VALUE);

        System.out.println("Мінімальне значення типу int: " + Integer.MIN_VALUE);
        System.out.println("Максимальне значення типу int: " + Integer.MAX_VALUE);

        System.out.println("Мінімальне значення типу long: " + Long.MIN_VALUE);
        System.out.println("Максимальне значення типу long: " + Long.MAX_VALUE);

        
        System.out.println("Мінімальне значення типу float: " + Float.MIN_VALUE);
        System.out.println("Максимальне значення типу float: " + Float.MAX_VALUE);

        System.out.println("Мінімальне значення типу double: " + Double.MIN_VALUE);
        System.out.println("Максимальне значення типу double: " + Double.MAX_VALUE);

        
        System.out.println("Мінімальне значення типу char: " + (int) Character.MIN_VALUE);
        System.out.println("Максимальне значення типу char: " + (int) Character.MAX_VALUE);

        
        System.out.println("Мінімальне значення типу boolean: " + Boolean.FALSE);
        System.out.println("Максимальне значення типу boolean: " + Boolean.TRUE);
    }
}